#include <iostream>

using namespace std;

int main(){
    int i;

    // mengulang aksi sebanyak 5 kali
    i = 0;
    while (i < 5) {
        cout << i << " "; // menampilkan nilai i dan satu spasi
        i++; // mneaikkan nilai i
    }

    return 0;
}
