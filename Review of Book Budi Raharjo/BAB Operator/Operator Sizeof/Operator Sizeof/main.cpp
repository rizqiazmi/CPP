#include <iostream>

using namespace std;

int main() {
    char c;
    int i;

    cout << "sizeof c\t: " << sizeof c << " byte" << endl;
    cout << "sizeof(i)\t: " << sizeof(i) << " byte" << endl;
    cout << "sizeof(float)\t: " << sizeof(float) << " byte" << endl;
    cout << "sizeof(double)\t: " << sizeof(double) << " byte" << endl;

    return 0;
}
