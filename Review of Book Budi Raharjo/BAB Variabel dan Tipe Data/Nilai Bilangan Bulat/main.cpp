#include <iostream>

using namespace std;

int main() {
    int a, b, c, d;

    a = 225; // ditulis dalam notasi desimal
    cout << "Nilai a: " << a << endl;

    b = 0b11111111; // ditulis dalam notasi biner
    cout << "Nilai b: " << b << endl;

    c = 0377; // ditulis dalam notasi oktal
    cout << "Nilai c: " << c << endl;

    d = 0xff; // ditulis dalam notasi heksadesimal
    cout << "Nilai d: " << d << endl;

    return 0;
}
